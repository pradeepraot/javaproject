public class BooleanExample {
    
    public static void main(String args[])
    {
        System.out.println("relational operators"+ (1>=3));  //relational operators
        
        if(1<3)  //control statements
        {
        System.out.println("control statements"+ (1<3));;  
        }
        
            
        System.out.println("equality operators "+(1==3));  //equality operators 
        System.out.println("logical operators"+(1>=3));  //logical operators
        System.out.println(1>=3);  //relational operators
        
    }
    
}
